package model;

public class ImageDetails {
	
	String src;
	int height;
	int width;
	
	public String getSrc() {
		return src;
	}
	public int getHeight() {
		return height;
	}
	public int getWidth() {
		return width;
	}
	public void setSrc(String src) {
		this.src = src;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	
}
