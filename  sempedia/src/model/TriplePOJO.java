package model;

public class TriplePOJO {
	
	private int tripleId;
	private int subId;
	private int preId;
	private int objId;
	
	public int getTripleId() {
		return tripleId;
	}
	public void setTripleId(int tripleId) {
		this.tripleId = tripleId;
	}
	public int getSubId() {
		return subId;
	}
	public void setSubId(int subId) {
		this.subId = subId;
	}
	public int getPreId() {
		return preId;
	}
	public void setPreId(int preId) {
		this.preId = preId;
	}
	public int getObjId() {
		return objId;
	}
	public void setObjId(int objId) {
		this.objId = objId;
	}
		
	
}
